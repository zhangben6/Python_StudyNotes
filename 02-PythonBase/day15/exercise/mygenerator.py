# 2) 写一个生成器表达式.让此表达式能够动态提供数据,提供
# 的数据为列表的数字的平方加1
for y in (x ** 2 + 1 for x in L):
    print(y)

# 3) 创建一个列表,此列表内的数据为原列表的数字的平方加1
L2 = [x ** 2 + 1 for x in L]
print(L2)

