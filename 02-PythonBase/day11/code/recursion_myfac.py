
# 此示例示意递归思想实现阶乘
# 5! = 5 * 4 * 3 * 2 * 1

# 5! = 5 * 4!
# 5! = 5 * 4 * 3!
# 5! = 5 * 4 * 3 * 2!
# 5! = 5 * 4 * 3 * 2 * 1!
# 5! = 5 * 4 * 3 * 2 * 1
# 5! = 5 * 4 * 3 * 2
# 5! = 5 * 4 * 6
# 5! = 5 * 24
# 5! = 120

def myfac(n):
    # 假设此函数已经实现
    if n == 1:  # 我知道1的阶乘是1,直接返回1,告诉调用者
        return 1
    # 如果是其它数,则让myfac()下一个阶乘的值,返回后
    # 再乘以当前值,给调用者
    return n * myfac(n - 1)


print(myfac(5))
