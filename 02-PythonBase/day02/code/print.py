

# 此示例示意print函数的用法
print(1, 2, 3, 4)
print(1, 2, 3, 4, sep=' ') # 默认
print(1, 2, 3, 4, sep='#')
print(1, 2, 3, 4, sep='###')
print(1, 2, 3, 4, sep='')
print(1, 2, 3, 4)
# print(1, 2, 3, 4, end='\n')
print(1, 2, 3, 4, end='')  # 常用于不换行
print(1, 2, 3, 4)
print(1, 2, 3, 4, end='\n\n\n')
print("aaaaaaaaaaaaaa")

